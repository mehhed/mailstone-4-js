var a = 10 ;
var b = 12 ;
// সমান কিনা জানেত
console.log(a == b);
// অসমান কিনা জানতে 
console.log(a != b);
// সমান এবং ভ্যারিএবল ধরন জানতে 
console.log(a === b);
// b এর মান a থেকে ছোট কিনা জানতে 
console.log(a < b);
 // b এর মান a থেকে বড় কিনা জানতে 
  console.log(a > b);
// b এর মান a থেকে ছোট অথবা সমান কিনা জানতে 
console.log(a <= b);
// a এর মান b থেকে বগ অথবা সমান কিনা জানতে 
console.log(a >= b);
// or যখন দুইটা শর্ত একসাথে জানতে চাওয়া হয় 
console.log(a = b || a < b);
// and 
console.log(a = b && a < b);